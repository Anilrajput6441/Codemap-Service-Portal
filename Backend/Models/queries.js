import mongoose from "mongoose";

const querySchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
    },
    phone: {
      type: String,
      required: true,
      trim: true,
    },
    message: {
      type: String,
      required: true,
      trim: true,
    },
  },
  {
    Collection: "queries",
  }
);

const queryModelObj = mongoose.model("queries", querySchema);
export default queryModelObj;
